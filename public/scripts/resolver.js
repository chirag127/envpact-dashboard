/**
 * Client-side resolver — port of envpact-cli/lib/resolver.js.
 *
 * Semantics match the CLI port (see SHARED_SPEC.md §1) with ONE intentional
 * divergence: the browser cannot run GPG, so encrypted (`enc:`) entries are
 * NEVER written into `resolved`. They are still tracked in the returned
 * `encrypted[]` array so the UI can surface them, but `.env` rendering and
 * any downstream consumer will simply omit those keys. To materialize
 * `enc:` values, use envpact-cli locally.
 */

export const SHARED_PREFIX = 'shared.';
export const ENC_PREFIX = 'enc:';

export function resolveString(rawValue, shared) {
  if (typeof rawValue !== 'string') return { value: null, status: 'invalid' };
  if (rawValue.startsWith(ENC_PREFIX)) return { value: rawValue, status: 'encrypted' };
  if (rawValue.startsWith(SHARED_PREFIX)) {
    const k = rawValue.slice(SHARED_PREFIX.length);
    if (!shared || !(k in shared)) return { value: null, status: 'unresolved' };
    const v = shared[k];
    if (typeof v !== 'string') return { value: null, status: 'invalid' };
    return v.startsWith(ENC_PREFIX)
      ? { value: v, status: 'encrypted' }
      : { value: v, status: 'ok' };
  }
  return { value: rawValue, status: 'ok' };
}

export function resolveProject(vault, projectName, environment) {
  const project = (vault.projects || {})[projectName];
  if (!project) return { resolved: {}, unresolved: [], invalid: [], environment: environment || 'default', missing: true };
  const effectiveEnv = environment || project._default_env || 'default';
  const resolved = {}, unresolved = [], invalid = [], encrypted = [];
  const shared = vault.shared || {};
  for (const [key, raw] of Object.entries(project)) {
    if (key.startsWith('_')) continue;
    let candidate;
    if (typeof raw === 'string') candidate = raw;
    else if (raw && typeof raw === 'object' && !Array.isArray(raw)) {
      if (effectiveEnv in raw) candidate = raw[effectiveEnv];
      else if ('default' in raw) candidate = raw.default;
      else { unresolved.push(key); continue; }
    } else { invalid.push(key); continue; }
    const r = resolveString(candidate, shared);
    if (r.status === 'ok') resolved[key] = r.value;
    else if (r.status === 'encrypted') { encrypted.push(key); }
    else if (r.status === 'unresolved') unresolved.push(key);
    else invalid.push(key);
  }
  return { resolved, unresolved, invalid, encrypted, environment: effectiveEnv, missing: false };
}

export function findReferencingProjects(vault, sharedKey) {
  const refs = [];
  const ref = `shared.${sharedKey}`;
  for (const [pname, proj] of Object.entries(vault.projects || {})) {
    for (const [k, v] of Object.entries(proj)) {
      if (k.startsWith('_')) continue;
      if (typeof v === 'string' && v === ref) refs.push({ project: pname, key: k });
      else if (v && typeof v === 'object') {
        for (const [env, ev] of Object.entries(v)) {
          if (typeof ev === 'string' && ev === ref) refs.push({ project: pname, key: k, environment: env });
        }
      }
    }
  }
  return refs;
}

export function downloadEnv(filename, content) {
  const blob = new Blob([content], { type: 'text/plain' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  setTimeout(() => { URL.revokeObjectURL(url); a.remove(); }, 100);
}

export function renderEnv(orderedKeys, valueMap, opts = {}) {
  const lines = [
    `# Generated by envpact-dashboard on ${new Date().toISOString()}`,
    '# DO NOT COMMIT — add .env to .gitignore',
  ];
  if (opts.environment) lines.push(`# environment: ${opts.environment}`);
  if (opts.project) lines.push(`# project: ${opts.project}`);
  lines.push('');
  for (const k of orderedKeys) {
    if (k in valueMap) {
      const v = String(valueMap[k]);
      const needsQuote = /[\s#"'\\]/.test(v) || v === '';
      lines.push(`${k}=${needsQuote ? `"${v.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"` : v}`);
    }
  }
  return lines.join('\n') + '\n';
}
